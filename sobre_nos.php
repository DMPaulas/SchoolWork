<p>O nascimento da Medi Clínica remonta a 1997, altura em que surge como uma pequena unidade de saúde familiar em Brejos de Azeitão.</p>
<p>Desde o seu início soube conquistar a confiança e a simpatia dos seus utentes, fidelizando-os com a qualidade do trabalho desenvolvido e com a excelência dos serviços prestados. Como consequência disso mesmo, essa unidade cresceu, levando à construção de uma clínica médica com as condições adequadas para receber, hoje em dia, cerca de 25.000 utentes por ano, muitos deles desde 97. É por isso que afirmamos com agrado que, nas nossas 14 horas de funcionamento diário, atendemos os nossos utentes e as sua famílias com a simpatia, profissionalismo e excelência de sempre.</p>
<p>Nesta altura a Medi Clínica tem por objetivo assumir-se, a nível local, como uma referência na prestação de serviços na área da saúde.</p>
<p>Temos uma estrutura idónea e qualificada, quer a nível de gestão, quer no que toca aos vários profissionais que connosco colaboram.</p>
<p>Apostamos na qualidade, experiência e empenho da nossa equipa, de forma a manter/criar essa importante relação de empatia e fidelidade com os nossos utentes/pacientes.</p>

